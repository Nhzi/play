export function kicksFor(id: PieceId, from: Rotation, to: Rotation): Cell[] {
  if (id === "O") return [[0, 0]];
  const key = `${from}->${to}`;
  return id === "I" ? KICKS_I[key] : KICKS_JLSTZ[key];
}
